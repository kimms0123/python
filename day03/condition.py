#1. 비밀번호 입력 프로그램
#!랑IT가 포함되면 올바르게 비밀번호 입력하였습니다 출력 아니면 올바르게 입력하지 않았습니다 출력
print("=============")
print("비밀번호 프로그램 시작")

password = input("비밀번호 입력>>")
if '!' in password and 'IT' in password:
    print("비밀번호를 올바르게 입력하였습니다.")
else:
    print("비밀번호를 올바르게 입력하지 않았습니다.")

print("비밀번호 프로그램 종료")
