#3. 비밀번호 설정
#'!'포함되지 않고, 첫 글자가 'a'또는 'A'이어야만 올바르게 설정되었습니다 출력 아니면 설정되지 않았습니다 출력
print("비밀번호 설정 프로그램")
password1 = input("비밀번호 입력>>")

if not('!' in password1) and (password1[0] == 'a' or password1[0] == 'A'):
    print("비밀번호를 올바르게 입력했습니다.")
else:
    print("비밀번호를 올바르게 입력하지 않았습니다.")

print("프로그램 종료")